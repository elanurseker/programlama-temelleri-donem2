tek_sayilar=[3,5,7,9,11,13,17,19]
print(tek_sayilar[0:6])#0'dan 6. listeye kadar olan liste elemanlarını yazdırır
#6 dahil değildir


#2. indeksten 5. indekse kadar yazdırır
print(tek_sayilar[2:5])
#5. dahil değildir


#3. indeksten 8. indekse kadar yazdırır
print(tek_sayilar[3:8])



#listenin başından iltibaren 4. elemana kadar olanlarını ekrana yazdırır
print(tek_sayilar[:5])
#5. eleman dahil değildir


#3. indeksten sonraki tüm elemanları yazdırır
print(tek_sayilar[3:])


#02dan başlayıp 8. indekse kadar 2şer şekilde yazdırır
print(tek_sayilar[0:8:2])


#listenin başından iltibaren 3er şekilde yazdırır
print(tek_sayilar[::3])