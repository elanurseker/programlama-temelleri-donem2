asal_sayilar=[2,3,5,7,11,13,17,19,23]
print(asal_sayilar[1:4])                #1. indeksten 4. indekse kadar olan liste elemanlarını yadırır
# 4. eleman dahil değildir


#5. indeksten sona kadar tüm liste elemanlarını yazdırır
print(asal_sayilar[5:])


#listenin başından itibaren 5. elemana kadar olanları yadırır
print(asal_sayilar[:6])


#0 dan 6. indekse kadar 2 şer 2şer yadırır
print(asal_sayilar[0:6:2])